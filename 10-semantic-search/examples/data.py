    # 推理加速
    "模型量化（Quantization）是将浮点权重压缩为低精度整数（INT8、INT4）的技术。"
    "量化后模型体积缩小 2~4 倍，推理延迟降低 30%~60%，精度损失通常在 1% 以内。"
    "主流方案有 GPTQ（训练后量化）和 QAT（量化感知训练）两种路线。",

    "TensorRT 是 NVIDIA 推出的推理加速库，支持 FP16 和 INT8 精度。"
    "它通过算子融合、内存优化和硬件特定指令集，将模型推理吞吐量提升 2~5 倍。"
    "主要用于 NVIDIA GPU 的生产部署环境。",

    "KV Cache 缓存注意力层的 Key 和 Value 矩阵，避免自回归生成时重复计算已有 token。"
    "对于长序列生成，KV Cache 能将推理速度提升数倍。"
    "代价是显存占用随序列长度线性增长，需要合理设置最大序列长度。",

    "批处理（Batching）将多个推理请求合并为一个批次处理，充分利用 GPU 并行计算能力。"
    "连续批处理（Continuous Batching）允许不同长度的请求动态拼接，进一步提升 GPU 利用率。"
    "vLLM 是实现连续批处理的代表性框架。",

    "Transformer 的自注意力机制时间复杂度是 O(n²)，长序列推理开销极大。"
    "FlashAttention 通过分块计算和 IO 感知设计，将显存占用从 O(n²) 降到 O(n)，速度提升 2~4 倍。"
    "FlashAttention-2 进一步优化了并行度和线程调度。",

    # 训练技术
    "梯度检查点（Gradient Checkpointing）以时间换空间：正向传播时不保留中间激活值，"
    "反向传播时重新计算。显存占用约减少 60%~70%，训练时间增加约 30%。"
    "是训练超大模型时的常用技巧。",

    "混合精度训练（Mixed Precision Training）用 FP16 做前向和反向传播，FP32 保存权重。"
    "在 NVIDIA GPU 上，FP16 矩阵乘法比 FP32 快约 8 倍，同时显存占用减半。"
    "需要配合损失缩放（Loss Scaling）防止梯度下溢。",

    "LoRA（Low-Rank Adaptation）通过低秩矩阵分解，只训练少量参数来微调大模型。"
    "典型配置只需训练原模型 0.1%~1% 的参数，显存需求大幅降低。"
    "QLoRA 进一步结合 4-bit 量化，使消费级 GPU 微调 70B 模型成为可能。",

    "分布式训练分为数据并行和模型并行两大类。"
    "数据并行（DDP）将同一模型复制到多个 GPU，每个 GPU 处理不同数据批次，梯度同步后更新。"
    "模型并行（流水线并行、张量并行）将模型切分到多个 GPU，用于超大模型的训练。",

    "学习率调度（Learning Rate Schedule）对训练收敛至关重要。"
    "Warmup 阶段从小学习率逐渐升高，避免训练初期梯度爆炸。"
    "余弦退火（Cosine Annealing）在训练后期平滑降低学习率，有助于找到更优的极小值。",

    # Embedding 和向量搜索
    "Sentence-BERT 通过孪生网络结构，让 BERT 能直接输出适合语义相似度计算的句向量。"
    "训练时使用自然语言推理（NLI）数据集，用余弦相似度损失优化。"
    "相比原始 BERT 的 [CLS] 向量，Sentence-BERT 的语义表示质量显著更高。",

    "向量数据库（Vector Database）专门为高维向量的近似最近邻（ANN）搜索设计。"
    "主流方案包括 Faiss、Milvus、Qdrant、Weaviate 和 ChromaDB。"
    "ANN 算法（HNSW、IVF 等）在精度和速度之间取得权衡，百亿级向量毫秒级响应。",

    "余弦相似度衡量两个向量方向的一致程度，取值范围 [-1, 1]。"
    "在语义搜索中，余弦相似度比欧氏距离更常用，因为它不受向量模长的影响。"
    "两段语义相关的文本，即使长度差异很大，其向量的余弦相似度也接近 1。",

    "HNSW（Hierarchical Navigable Small World）是目前性能最好的 ANN 索引结构之一。"
    "通过构建多层图结构，从高层快速定位大致区域，再在低层精细搜索。"
    "查询复杂度约为 O(log n)，适合实时搜索场景。",

    # LLM 基础
    "大语言模型（LLM）的预训练目标是下一个 token 预测（Next Token Prediction）。"
    "模型在海量文本上学习语言的统计规律，形成对世界知识的隐式编码。"
    "预训练后的模型具备基础语言能力，但需要指令微调才能遵循用户指令。",

    "指令微调（Instruction Tuning）在预训练模型基础上，用（指令, 回答）对进行有监督训练。"
    "让模型学会理解和遵循各类自然语言指令，是 ChatGPT 类产品的关键步骤。"
    "RLHF（人类反馈强化学习）进一步对齐模型输出与人类偏好。",

    "Transformer 架构由 Vaswani 等人在 2017 年论文《Attention Is All You Need》中提出。"
    "完全基于注意力机制，抛弃了 RNN 的序列依赖，训练可高度并行化。"
    "现在几乎所有主流语言模型（GPT、BERT、LLaMA）都基于 Transformer 或其变体。",

    "上下文窗口（Context Window）决定了模型单次能处理的最大 token 数。"
    "早期 GPT-3 的上下文窗口为 4096 token，Claude 和 Gemini 已扩展到 100K~1M token。"
    "长上下文模型在推理时显存和计算开销都随序列长度显著增长。",

    "Tokenization 是将原始文本切分为 token 序列的过程。"
    "BPE（Byte-Pair Encoding）是最常见的分词算法，通过合并高频字节对构建词表。"
    "中文每个字通常对应 1~2 个 token，英文单词平均约 1.3 个 token。",

    "RAG（Retrieval-Augmented Generation）将外部知识检索与语言模型生成结合。"
    "解决了 LLM 知识截止和幻觉两大核心问题，是目前最实用的 LLM 应用架构。"
    "典型流程：用户问题 → 向量检索相关文档 → 将文档塞入 prompt → LLM 生成回答。",
